<h1>Leistungsspektrum</h1>
<br /><br />

<h2>Vorsorge und Beratung</h2>
<div class="pt-2 pb-6 text-justify w-full">
    Hausärztliche Versorgung inkl. Hausbesuche, Gesundheitscheck, Krebsvorsorge, Tauglichkeitsuntersuchungen,
    Führerscheinuntersuchungen exklusive Sehtest, psychosomatische Grundversorgung, Impfungen mit Beratung
</div>

<h2>Diagnostik</h2>

<div class="pt-2 pb-6 text-justify w-full">
    EKG, 24-h-EKG, 24-h-Blutdruckmessung, Lungenfunktion, Ultraschall Bauchraum (Leber, Gallenblase, Bauchspeicheldrüse,
    Nieren, Milz, Prostata, Bauchschlagader), Ultraschall der Schilddrüse, Ultraschallscreening auf Bauchaortenaneurysma
    beim Männern ab 65 Jahren (einmalig), Endokrinologie (z. B Schilddrüse, Diabetes mellitus, Osteoporose),
    Stoffwechsel (z. B. Diabets, Cholesterin) Laboruntersuchungen
</div>

<h2>Spezielle Erkrankungen</h2>

<div class="pt-2 pb-6 text-justify w-full">
    Erkrankungen des Respirationstraktes, Erkrankungen der Niere und der Harnwege, Erkrankungen des Immunsystems, des
    Bindegewebes und der Gelenke, Erkrankungen des Verdauungstraktes (z. B. Leber, Magen, Darm), Tumorerkrankungen
    (Behandlung und Betreuung von Krebspatienten)
</div>

<h2>Therapie</h2>

<div class="pt-6 pb-6 text-justify w-full">
    Notfallmedizin, Schmerztherapie, Infektionskrankheiten inkl. Reisemedizin, Disease Management Programme (DMP)
</div>

<h2>Diagnostik</h2>

<div class="pt-6 text-justify w-full">
    Intensive Behandlung im Rahmen der DMP-Programme für Diabetes mellitus, KHK-koronare Herzerkrankungen,
    COPD-chronische Lungenerkrankungen, Asthma bronchiale - Individuelle Terminvereinbarungen sind möglich für:
    Gesundheits-Check, Krebsvorsorge, Belastungs-EKG, Impfungen und Beratungen Reisemedizin, Psychosomatische
    Grundversorgung, Terminsprechstunde Dienstag und Donnerstag Nachmittag.
</div>
